    int age = 20;   // int = keyword, age => identifier, = = operator, 20 = literal, ; = punctuation
    float grade = 20.7; // float = keyword, grade = identifier, = = operator, 20.7 = literal, ; = punctuation
    char grade = 'A';  // char = keyword, grade = identifier, = = operator, A = literal, ; = punctuation

    printf("Integer: <%d>\n", level);
