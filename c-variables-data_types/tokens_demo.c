#include <stdio.h>

int main() {
    int level = 300;   // int => keyword, i => identifier, = => operator, 35 => literal, ; => punctuation
    float cgpa = 4.72; // float => keyword, cgpa => identifier, = => operator, 4.72 => literal, ; => punctuation
    char grade = 'A';  // char =. keyword, age => identifier, = => operator, 20 => literal, ; => punctuation

    printf("Integer: %d\n", level);
    printf("Float: %f\n", cgpa);
    printf("Char: %c", grade);

    return 0;
}

